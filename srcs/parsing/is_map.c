#include "l_libc.h"

t_bool	is_map(char c)
{
	if (c == '0' ||
		c == '1' ||
		c == '2' ||
		c == 'N' ||
		c == 'S' ||
		c == 'E' ||
		c == 'W' )
		return (TRUE);
	return (FALSE);
}
